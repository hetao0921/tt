package com.fxdigital.video.ctrl;

public class DC_VIDEO_COMPRESS { 
	// 幀率
	public int m_nFramerate;

	// 码率
	public int m_nBitrate;

	// 图像质量
	public int m_nQuality;

	// 分辨率
	public int m_nResolution;

	// 编码类型
	public int m_nCodecType;

	// 是否为定码率、或变码率
	public boolean m_bConstBitrate;

	// 是否为标准编码、或厂商私有
	public boolean m_bStdCodec;

	public int getM_nFramerate() {
		return m_nFramerate;
	}

	public void setM_nFramerate(int m_nFramerate) {
		this.m_nFramerate = m_nFramerate;
	}

	public int getM_nBitrate() {
		return m_nBitrate;
	}

	public void setM_nBitrate(int m_nBitrate) {
		this.m_nBitrate = m_nBitrate;
	}

	public int getM_nQuality() {
		return m_nQuality;
	}

	public void setM_nQuality(int m_nQuality) {
		this.m_nQuality = m_nQuality;
	}

	public int getM_nResolution() {
		return m_nResolution;
	}

	public void setM_nResolution(int m_nResolution) {
		this.m_nResolution = m_nResolution;
	}

	public int getM_nCodecType() {
		return m_nCodecType;
	}

	public void setM_nCodecType(int m_nCodecType) {
		this.m_nCodecType = m_nCodecType;
	}

	public boolean isM_bConstBitrate() {
		return m_bConstBitrate;
	}

	public void setM_bConstBitrate(boolean m_bConstBitrate) {
		this.m_bConstBitrate = m_bConstBitrate;
	}

	public boolean isM_bStdCodec() {
		return m_bStdCodec;
	}

	public void setM_bStdCodec(boolean m_bStdCodec) {
		this.m_bStdCodec = m_bStdCodec;
	}
	
	
	
	
}
