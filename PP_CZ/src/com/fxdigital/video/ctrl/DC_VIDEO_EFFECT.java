package com.fxdigital.video.ctrl;

public class DC_VIDEO_EFFECT { 
	// 色度
	public int m_nHue;

	// 对比度
	public int m_nContrast;

	// 亮度
	public int m_nBirghtness;

	// 饱和度
	public int m_nSaturation;

	public int getM_nHue() {
		return m_nHue;
	}

	public void setM_nHue(int m_nHue) {
		this.m_nHue = m_nHue;
	}

	public int getM_nContrast() {
		return m_nContrast;
	}

	public void setM_nContrast(int m_nContrast) {
		this.m_nContrast = m_nContrast;
	}

	public int getM_nBirghtness() {
		return m_nBirghtness;
	}

	public void setM_nBirghtness(int m_nBirghtness) {
		this.m_nBirghtness = m_nBirghtness;
	}

	public int getM_nSaturation() {
		return m_nSaturation;
	}

	public void setM_nSaturation(int m_nSaturation) {
		this.m_nSaturation = m_nSaturation;
	}

}
