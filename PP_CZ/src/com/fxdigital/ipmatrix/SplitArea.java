package com.fxdigital.ipmatrix;

public class SplitArea{  
	private int nIndex;//当前分区编号
	private int nDecoderIndex;//解码器索引
	private int nIsDecode;//-1,0,1,是否正在解码
	private int nIsAreaPlay;//-1,0,1,当前分屏是否正在输出
	private String sourceIp;
	private int sourcePort;
	private int sourceChannel;
	public int getnIndex() {
		return nIndex;
	}
	public void setnIndex(int nIndex) {
		this.nIndex = nIndex;
	}
	public int getnDecoderIndex() {
		return nDecoderIndex;
	}
	public void setnDecoderIndex(int nDecoderIndex) {
		this.nDecoderIndex = nDecoderIndex;
	}
	public int getnIsDecode() {
		return nIsDecode;
	}
	public void setnIsDecode(int nIsDecode) {
		this.nIsDecode = nIsDecode;
	}
	public int getnIsAreaPlay() {
		return nIsAreaPlay;
	}
	public void setnIsAreaPlay(int nIsAreaPlay) {
		this.nIsAreaPlay = nIsAreaPlay;
	}
	public String getSourceIp() {
		return sourceIp;
	}
	public void setSourceIp(String sourceIp) {
		this.sourceIp = sourceIp;
	}
	public int getSourcePort() {
		return sourcePort;
	}
	public void setSourcePort(int sourcePort) {
		this.sourcePort = sourcePort;
	}
	public int getSourceChannel() {
		return sourceChannel;
	}
	public void setSourceChannel(int sourceChannel) {
		this.sourceChannel = sourceChannel;
	}
}
