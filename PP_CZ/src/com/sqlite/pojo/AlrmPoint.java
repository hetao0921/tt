package com.sqlite.pojo;


public class AlrmPoint extends BasePO{
	private int aid;                  //主键ID 
	private String deviceId;          //设备ID
	private String deviceName;        //设备名称
	private int channelID;            //通道号
	private int deviceStatus;         //通道状态	
	
	public AlrmPoint() {
		super();
	}

	public AlrmPoint(int aid, String deviceId, String deviceName,
			int channelID, int deviceStatus) {
		super();
		this.aid = aid;
		this.deviceId = deviceId;
		this.deviceName = deviceName;
		this.channelID = channelID;
		this.deviceStatus = deviceStatus;
	}

	public int getAid() {
		return aid;
	}

	public void setAid(int aid) {
		this.aid = aid;
	}

	public String getDeviceId() {
		return deviceId;
	}

	public void setDeviceId(String deviceId) {
		this.deviceId = deviceId;
	}

	public String getDeviceName() {
		return deviceName;
	}

	public void setDeviceName(String deviceName) {
		this.deviceName = deviceName;
	}

	public int getChannelID() {
		return channelID;
	}

	public void setChannelID(int channelID) {
		this.channelID = channelID;
	}

	public int getDeviceStatus() {
		return deviceStatus;
	}

	public void setDeviceStatus(int deviceStatus) {
		this.deviceStatus = deviceStatus;
	}
	
}
