package fxdigital.mqcore.exchange.impl;

public class UpConfig {
	
	String upIp;
	int upPort;
	String upChannelName;
	String filterString;
	

	public String getUpIp() {
		return upIp;
	}

	public void setUpIp(String upIp) {
		this.upIp = upIp;
	}

	public int getUpPort() {
		return upPort;
	}

	public void setUpPort(int upPort) {
		this.upPort = upPort;
	}



	public String getUpChannelName() {
		return upChannelName;
	}

	public void setUpChannelName(String upChannelName) {
		this.upChannelName = upChannelName;
	}

	public String getFilterString() {
		return filterString;
	}

	public void setFilterString(String filterString) {
		this.filterString = filterString;
	}

	
	
	
}
