package com.fxdigital.filesync.bean;

public class FileSyncInfoBean {
	
	public String mainserverpath;
	public String backupserverpath;
    public int filesyncdelay;
    public int filesyncinterval;
	public String getMainserverpath() {
		return mainserverpath;
	}
	public void setMainserverpath(String mainserverpath) {
		this.mainserverpath = mainserverpath;
	}
	public String getBackupserverpath() {
		return backupserverpath;
	}
	public void setBackupserverpath(String backupserverpath) {
		this.backupserverpath = backupserverpath;
	}
	public int getFilesyncdelay() {
		return filesyncdelay;
	}
	public void setFilesyncdelay(int filesyncdelay) {
		this.filesyncdelay = filesyncdelay;
	}
	public int getFilesyncinterval() {
		return filesyncinterval;
	}
	public void setFilesyncinterval(int filesyncinterval) {
		this.filesyncinterval = filesyncinterval;
	}
    
    

}
