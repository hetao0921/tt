public class TableData {
	
	private int page;
	private int total;
	private long records;
	private List<UserLogBean> rows;
	
	public int getPage() {
		return page;
	}
	public void setPage(int page) {
		this.page = page;
	}
	public int getTotal() {
		return total;
	}
	public void setTotal(int total) {
		this.total = total;
	}
	public long getRecords() {
		return records;
	}
	public void setRecords(long records) {
		this.records = records;
	}
	public List<UserLogBean> getRows() {
		return rows;
	}
	public void setRows(List<UserLogBean> rows) {
		this.rows = rows;
	}
	
}
