package com.fxdigital.db.pojo;

public class WsdlPojo {
	private int wsdlId;
	private int wsdlInfoId;
	private String wsdlURI;
	private String wsdlDesc;
	private String functionName;
	private String functionDesc;
	private String arguments;
	private String results;
	public String getWsdlURI() {
		return wsdlURI;
	}
	public int getWsdlId() {
		return wsdlId;
	}
	public void setWsdlId(int wsdlId) {
		this.wsdlId = wsdlId;
	}
	public int getWsdlInfoId() {
		return wsdlInfoId;
	}
	public void setWsdlInfoId(int wsdlInfoId) {
		this.wsdlInfoId = wsdlInfoId;
	}
	public void setWsdlURI(String wsdlURI) {
		this.wsdlURI = wsdlURI;
	}
	public String getWsdlDesc() {
		return wsdlDesc;
	}
	public void setWsdlDesc(String wsdlDesc) {
		this.wsdlDesc = wsdlDesc;
	}
	public String getFunctionName() {
		return functionName;
	}
	public void setFunctionName(String functionName) {
		this.functionName = functionName;
	}
	public String getFunctionDesc() {
		return functionDesc;
	}
	public void setFunctionDesc(String functionDesc) {
		this.functionDesc = functionDesc;
	}
	public String getArguments() {
		return arguments;
	}
	public void setArguments(String arguments) {
		this.arguments = arguments;
	}
	public String getResults() {
		return results;
	}
	public void setResults(String results) {
		this.results = results;
	}
	
	

}
