package com.fxstore.ctrl;

public class FxResult {
	private int nRecordId;
	private int nChannel;
	private String Memo;
	private String RealDeviceIds;
	private String FilePath;
	private String StartTime; 
	private String EndTime;

	public int getnRecordId() {
		return nRecordId;
	}

	public void setnRecordId(int nRecordId) {
		this.nRecordId = nRecordId;
	}

	public int getnChannel() {
		return nChannel;
	}

	public void setnChannel(int nChannel) {
		this.nChannel = nChannel;
	}

	public String getMemo() {
		return Memo;
	}

	public void setMemo(String memo) {
		Memo = memo;
	}

	public String getRealDeviceIds() {
		return RealDeviceIds;
	}

	public void setRealDeviceIds(String realDeviceIds) {
		RealDeviceIds = realDeviceIds;
	}

	public String getFilePath() {
		return FilePath;
	}

	public void setFilePath(String filePath) {
		FilePath = filePath;
	}

	public String getStartTime() {
		return StartTime;
	}

	public void setStartTime(String startTime) {
		StartTime = startTime;
	}

	public String getEndTime() {
		return EndTime;
	}

	public void setEndTime(String endTime) {
		EndTime = endTime;
	}

}
