package corenet.netbase;

import corenet.netbase.Interface.IOHandler;

public class IAsyncResult {
	private IOHandler ioHandler;
	private AsyncBuffer asyncBuffer; 

	public IAsyncResult(IOHandler io, AsyncBuffer ab) {
		this.ioHandler = io;
		this.asyncBuffer = ab;
	}

	public IOHandler getIoHandler() {
		return ioHandler;
	}

	public AsyncBuffer getAsyncBuffer() {
		return asyncBuffer;
	}

}
