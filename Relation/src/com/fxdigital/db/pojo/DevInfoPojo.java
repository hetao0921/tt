package com.fxdigital.db.pojo;

/**
 * 
 * @author hxht
 * @version 2014-9-15
 */
public class DevInfoPojo {
	
	private long id;
	private String name;
	private String url;
	private String desc;
	
	public long getId() {
		return id;
	}
	public void setId(long id) {
		this.id = id;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getUrl() {
		return url;
	}
	public void setUrl(String url) {
		this.url = url;
	}
	public String getDesc() {
		return desc;
	}
	public void setDesc(String desc) {
		this.desc = desc;
	}
	
}
